$totalSize = ($inventoryRows | Measure-Object -Property file_size_bytes -Sum).Sum
$byFolder = $inventoryRows | Group-Object folder | Sort-Object Name
$byClass = $inventoryRows | Group-Object classification | Sort-Object Name

